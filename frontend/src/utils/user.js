import { getStorage, setStorage } from './cache/storage.js'
import { random } from 'lodash-es'
export const getUserInfo = () => {
  let userInfo = getStorage('userInfo')
  if (!userInfo) {
    userInfo = { id: random(10000000, 99999999, false), nickName: getRadomNickName() }
    setStorage('userInfo', userInfo)
  }
  return userInfo
}


